// 5.「助かった！」に添える、渡さなかった2人も間に合ったことを示す一文
function buildRescuedExtraLine(notSelectedIds){
  const [a, b] = orderedRoleLabels(notSelectedIds);
  return "今回食料を渡さなかった" + a + "と" + b + "も、ぎりぎりのところで間に合いました。";
}

// 「食料を渡した2人」「渡さなかった2人」から、紙芝居のページ構成を組み立てる
function buildSuccessPages(decision2){
  const selected = decision2.slice(0, FOOD_RECIPIENTS);
  const notSelected = decision2.slice(FOOD_RECIPIENTS);
  const pages = [];
  notSelected.forEach(id => pages.push({ type:"immobile", id }));
  selected.forEach(id => pages.push({ type:"helped", id }));
  pages.push({ type:"rescued", notSelected });
  CHARACTERS.forEach(c => pages.push({ type:"gratitude", id:c.id }));
  return pages;
}

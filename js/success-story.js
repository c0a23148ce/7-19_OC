function buildSuccessPages(decision2){
  const selected = decision2.slice(0, FOOD_RECIPIENTS);
  const notSelected = decision2.slice(FOOD_RECIPIENTS);
  const pages = [];
  notSelected.forEach(id => pages.push({ type:"survived", id }));
  selected.forEach(id => pages.push({ type:"helped", id }));
  pages.push({ type:"final" });
  return pages;
}
